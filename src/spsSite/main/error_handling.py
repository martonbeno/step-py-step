def is_error_message(msg):
	ALL_ERRORS = [
	'ArithmeticError',
	'AssertionError',
	'AttributeError',
	'BaseException',
	'BlockingIOError',
	'BrokenPipeError',
	'BufferError',
	'ChildProcessError',
	'ConnectionAbortedError',
	'ConnectionError',
	'ConnectionRefusedError',
	'ConnectionResetError',
	'EOFError',
	'EnvironmentError',
	'Exception',
	'FileExistsError',
	'FileNotFoundError',
	'FloatingPointError',
	'IOError',
	'ImportError',
	'ImportWarning',
	'IndentationError',
	'IndexError',
	'InterruptedError',
	'IsADirectoryError',
	'KeyError',
	'KeyboardInterrupt',
	'LookupError',
	'MemoryError',
	'ModuleNotFoundError',
	'NameError',
	'NotADirectoryError',
	'NotImplemented',
	'NotImplementedError',
	'OSError',
	'OverflowError',
	'PermissionError',
	'ProcessLookupError',
	'RecursionError',
	'ReferenceError',
	'RuntimeError',
	'RuntimeWarning',
	'StopAsyncIteration',
	'StopIteration',
	'SyntaxError',
	'SyntaxWarning',
	'SystemError',
	'SystemExit',
	'TabError',
	'TimeoutError',
	'TypeError',
	'UnboundLocalError',
	'UnicodeDecodeError',
	'UnicodeEncodeError',
	'UnicodeError',
	'UnicodeTranslateError',
	'ValueError',
	'ZeroDivisionError']

	return any(msg.startswith(e) for e in ALL_ERRORS)